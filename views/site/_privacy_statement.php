<h2>Privacy Statement</h2>
<h3>Personal Data</h3>
<p>
The only personal data that Antlog uses is your email address, which you provide when you create an account.
Your email address is only ever used to send you a password reset link when you request one.
</p>
<p>
Email addresses are only stored on the antlog.garya.org.uk server. When data is downloaded for use at an event
email addresses are removed from the user/team data.
</p>
<p>
If a user/team is created at an event the email address is stored on the event server which is the
responsibility of the Event Organiser. The email address is then transferred to the antlog.garya.org.uk server
after the event along with the event results.
</p>
<h3>Viewing and Updating Your Personal Data</h3>
<p>
You can view the personal data that Antlog holds about you. First you have to log in, then click on your username in
the menu bar at the top of the page. Click on Details in the drop-down menu which enables you to see and edit all of
your user data.
</p>
<p>
You can update your email address at any time. You can also change your username and team name, but remember that the
team name appears in the results of any previous events that you have entered, so changing your team name unnecessarily
may confuse other users.
</p>
<h3>Your Right To Be Forgotten</h3>
<p>
You can delete your account by clicking on the Delete button that appears on your team page. Note that this will delete
all your user/team data and there is no way back! The robots belonging to your team will also be deleted if they have
never entered any events. Otherwise they will remain in the database (in an inactive state) so that the results of
historic events are preserved. Your team details will be deleted and your robots will show as belonging to Deleted Team
(xxx) where xxx is a number.
</p>