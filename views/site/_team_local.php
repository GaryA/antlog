<h3>Password</h3>
<p>
At an event, all team passwords are set to &quot;password&quot; (so that anyone downloading
the database does not have access to the online passwords). If you change your password at an
event is will be changed online when the results are uploaded.
</p>
<h3>Team Management</h3>
<p>
You can create a new team, update team details, create and update robots, and sign robots up
for the current event. Any changes will be changed online when the results of the event are
uploaded.
</p>
<h3>Event Sign-up</h3>
<p>
Teams can sign up robots for the event and delete any robots that have been entered but will
not be competing. The event organiser will confirm each entry at tech checks. Go to the event
page and click the &quot;Entrants&quot; button to view the entries for the event and their status.
</p>
<h3>Viewing Fights</h3>
<p>
Forthcoming fights can be viewed by clicking the &quot;Fights&quot; button on the event page. The
results of completed fights can also be seen by clicking on the link at the top of the page.
The page will auto-refresh every 30 seconds so that it is always up to date.
</p>
<p>
The forthcoming fights for an individual team can be seen by going to the team&apos;s page.
The results of previous fights can be seen for each robot by clicking on the robot name in the
&quot;Previous Fights&quot; section.
</p>
<p>
During the competition, the event&apos;s Entrants page will show the robots in their groups and
whether each robot is in the winners&apos; bracket, losers&apos; bracket, or out of the competition.
</p>
<h3>Viewing the Results</h3>
<p>
At the end of the competition the results table can be viewed, showing placings for all robots that
progress beyond the group stages.
</p>