<h3>Tech Checks</h3>
<p>
At the tech checks each entrant can be confirmed. Any no-shows can be deleted before doing the draw.
The list of entrants can be filtered by selecting a single team from a drop-down list. The list can
also be filtered by status, so only &quot;Signed Up&quot; robots - i.e. those that have not been checked -
will be displayed. An individual robot can be searched for by entering part or all of its&apos; name.
</p>
<h3>Doing The Draw</h3>
<p>
The draw can only be done when all the entrants have been confirmed, so make sure any no-shows have
been deleted. The draw process shows a progress bar; the progress is non-linear and depends on the
number of entrants in the competition. If there are no single-robot teams the draw process may fail.
In this case the draw can be re-run until a solution is found.
</p>
<h3>Starting The Competition</h3>
<p>
To start the competition, click the &quot;Run Fights&quot; button. The process of setting up the fights
and running any first round byes shows a progress bar; the progress is non-linear and depends on the
number of entrants in the competition.
</p>
<h3>Running Fights</h3>
<p>
To run a fight, click on the &quot;vs&quot; button between the robot names. To record the result of
the fight, click on the name of the winning robot. If a fight is delayed, click the &quot;Close&quot;
button and select a different fight to run.
</p>
<h3>Correcting Results</h3>
<p>
If a result is entered incorrectly it can be corrected as long as no subsequent fights involving
either of the two robots in the first fight have been run. Click the &quot;Show completed fights&quot;
link at the top of the fights page. Completed fights will show a &quot;Change&quot; button instead of
a &quot;vs&quot; button. If the result can be changed, the existing result will be shown with a
&quot;Change&quot; button. If the result cannot be changed a message to that effect will be displayed.
</p>
<h3>Completing an Event</h3>
<p>
When the final is reached Antlog will automatically re-run the final if necessary to complete the
double elimination. When the event is complete the results page will be displayed with placings shown
for all robots that progressed beyond the group stages (i.e. the last 16 in the case of an 8-group
event).
</p>
<h3>Saving the Results</h3>
<p>
Antlog automatically stores fight results to the database as they are entered. When the event is over,
click &quot;Export&quot; on the menu to download the database and follow the instructions to get it
added to the web database.
</p>